        <h1 class="titles">Our History</h1>
        <p class="gray-background">
          The NJIT Chapter of Hispanic Organization of Students in Technology/ Society of Hispanic Professional Engineers (HOST/SHPE) has a long history of dedication to its members and the community.
        </p>
        <h1 class="titles">OLA Years</h1>
        <p class="gray-background">
          In 1982, OLA (Organization Latino America) was created on the NJIT Campus. 
          The OLA was dedicated to promoting higher educational level for the Hispanic student and motivating them to pursue a technological career.
          OLA sponsored recreational, social, and academic activities that brought the culture,language, and customs of Hispanics into focus for everyone to enjoy.</p>
        </p>
        <h1 class="titles">HOST Years</h1>
        <p class="gray-background">
          However, in 1985, in an effort to embrace the math, engineering, and scientific nature of our university, OLA changed its name to HOST (Hispanic Organization of Students in Technology). As HOST, the organization not only dealt with Hispanic culture and history but also branched out into the technological world. Four years later, an entirely different Hispanic Organization name SHPE (Society of Hispanic Professional Engineers) brought a chapter to NJIT. 
          SHPE, a national organizational formed to serve as a model in the community, has been around since 1974 nationally.
        </p>
        <h1 class="titles">HOST/SHPE Alliance</h1>
        <p class="gray-background">
          Even though HOST and SHPE shared the same goals and ideology, they were separate until 1991 when both organizations merged unto one and became known as HOST/SHPE. 
          Since then, HOST/SHPE members have been involved in cultural events on and off campus, numerous community service activities, and all of SHPE's regional and national conferences. 
          Moreover, HOST/SHPE has won a number of regional and national awards and it is widely recognized for educating, supporting, and instilling a sense of pride in aspiring students in the fields of engineering, technology, and science. 
          As a matter of fact, HOST/SHPE received the Organization of the Year Award at the Newark College of Engineering Banquet on Friday, April 2,2004, alongside several congressional letters, national, and university awards.
        </p>
